    public void lista() {
        try {

            List<String> lineas = Files.readAllLines(Paths.get("C:\\Users\\Carlo\\OneDrive\\Documentos\\TERCERO A\\Preogramacion Visual\\archivo.txt"));
            lineas.stream().map(linea -> linea.split(" ")).filter(datos -> datos.length >= 3).forEach(datos -> {
                String nombre = datos[0];
                String apellido1 = datos[1];
                String apellido2 = datos[2];

                Persona persona = new Persona();
                persona.setNombre(nombre);
                persona.setApellido1(apellido1);
                persona.setApellido2(apellido2);

                personas.add(persona);
            });

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public void ordenar() {

        List<Persona> personasOrdenadas = personas.stream()
                .sorted(Comparator.comparing(Persona::getApellido1))
                .collect(Collectors.toList());

        String listaPersonas = personasOrdenadas.stream()
                .map(persona -> persona.getApellido1() + " " + persona.getApellido2() + " " + persona.getNombre())
                .collect(Collectors.joining("\n"));

        jTextArea1.setText(listaPersonas);

    }

    public void mostrar() {

        lista();

        String listaPersonas = personas.stream().map(persona -> persona.getNombre() + " " + persona.getApellido1() + " " + persona.getApellido2()).collect(Collectors.joining("\n"));

        txtListaPersonas.setText(listaPersonas);
    }

    public void buscar() {

        lista();

        String nombreBuscado = txtBuscaNombre.getText();
        String apellido1Buscado = txtApellidoP.getText();
        String apellido2Buscado = txtApellidoM.getText();

        Optional<Persona> personaEncontrada = personas.stream().filter(persona -> persona.getNombre().equals(nombreBuscado) && persona.getApellido1().equals(apellido1Buscado)
                && persona.getApellido2().equals(apellido2Buscado)).findFirst();

        personaEncontrada.ifPresentOrElse(persona -> txtResultadoBusqueda.setText("Persona encontrada: " + persona.getNombre() + " " + persona.getApellido1() + " "
                + persona.getApellido2()), () -> txtResultadoBusqueda.setText("No se encontr√≥ ninguna persona con los datos buscados."));

    }

